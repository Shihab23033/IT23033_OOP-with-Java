public class CustomPrintCLass {

    public static void pr(String abc){
        System.out.print(abc);
    }
    public static void prln(String abc){
        System.out.println(abc);
    }
    public static void pr(int abc){
        System.out.print(abc);
    }
    public static void pr(float abc){
        System.out.print(abc);
    }
    public static void pr(double abc){
        System.out.print(abc);
    }
    
}